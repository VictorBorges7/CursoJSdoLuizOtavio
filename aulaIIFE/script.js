// IIFE -> Immediately Invoked Function Expression, Funcoes imediatamente invocadas;

(function(){
    const nome = 'Victor'

    console.log(nome);
})();

const nome = 'Rayssa'
console.log(nome);

// Sua sintaxe é composta por parenteses, uma funcao anonima e parenteses novamente apos os primeiros parenteses.
// Essa funcao já é chamada e funcionada logo apos sua declaracao;
// Ela tambem aceita parametros